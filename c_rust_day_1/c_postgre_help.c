/*
 * printf() function prototype is available in 
 * stdio.h header file. 
 * Here, we are including header file using include macro
 */
#include <stdio.h>

/*
 * Entry point of the C program is main function.
 * Using return type as int as per modern C convention
 */
int
main() {

    /*
     * printf(...) is used to print the data on console.
     * Here, we are printing raw string.
     */
    printf("postgres is the PostgreSQL server.\n\n");

    return 0;
}
