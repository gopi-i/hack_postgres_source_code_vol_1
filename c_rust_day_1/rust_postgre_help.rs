/*
 * In rust, we have entry point of program as `fn main()`
 */

fn main() {
    
    /*
     * println! is macro which is used to print the data.
     * If we omit `!` in `println!` then it will be treated as function
     * and compiler tries to find out the function in program.
     */
    println!("postgres is the PostgreSQL server.\n\n");

}
